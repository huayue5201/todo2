-- lua/todo2/task/init.lua
--- @module todo2.task
--- @brief 任务管理模块

local M = {}

---------------------------------------------------------------------
-- 直接依赖
---------------------------------------------------------------------
local config = require("todo2.config")
local status = require("todo2.status")

---------------------------------------------------------------------
-- 模块依赖声明
---------------------------------------------------------------------
M.dependencies = {
	"status",
	"store",
	"ui",
	"render",
}

---------------------------------------------------------------------
-- 配置管理
---------------------------------------------------------------------
function M.setup()
	-- 初始化状态模块的其他功能
	if status and status.setup then
		status.setup()
	end

	return M
end

return M
